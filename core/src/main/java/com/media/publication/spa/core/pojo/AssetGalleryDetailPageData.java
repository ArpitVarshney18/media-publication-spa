package com.media.publication.spa.core.pojo;

public class AssetGalleryDetailPageData {

	private String assetOwnerName;
	private String assetOwnerEmail;
	private String assetDownloadLink;
	private String assetType;
	private String assetImage;

	public String getAssetOwnerName() {
		return assetOwnerName;
	}

	public void setAssetOwnerName(String assetOwnerName) {
		this.assetOwnerName = assetOwnerName;
	}

	public String getAssetOwnerEmail() {
		return assetOwnerEmail;
	}

	public void setAssetOwnerEmail(String assetOwnerEmail) {
		this.assetOwnerEmail = assetOwnerEmail;
	}

	public String getAssetDownloadLink() {
		return assetDownloadLink;
	}

	public void setAssetDownloadLink(String assetDownloadLink) {
		this.assetDownloadLink = assetDownloadLink;
	}

	public String getAssetType() {
		return assetType;
	}

	public void setAssetType(String assetType) {
		this.assetType = assetType;
	}

	public String getAssetImage() {
		return assetImage;
	}

	public void setAssetImage(String assetImage) {
		this.assetImage = assetImage;
	}



}
